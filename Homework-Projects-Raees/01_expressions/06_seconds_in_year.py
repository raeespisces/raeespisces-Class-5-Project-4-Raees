def seconds_in_year():
    seconds_in_minute = 60
    minutes_in_hour = 60
    hours_in_day = 24
    days_in_year = 365
    seconds_in_year = seconds_in_minute * minutes_in_hour * hours_in_day * days_in_year
    print(f"There are {seconds_in_year} seconds in a year")
       

if __name__ == "__main__":
    seconds_in_year()                   

